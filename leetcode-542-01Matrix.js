var updateMatrix = function (matrix) {
  let m = matrix.length,
    n = matrix[0].length;
  let queue = [];
  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (matrix[i][j] === 0) {
        queue.push([i, j]);
      } else {
        matrix[i][j] = Infinity;
      }
    }
  }

  console.log(matrix);

  let dirs = [
    [-1, 0],
    [1, 0],
    [0, -1],
    [0, 1],
  ];
  while (queue.length) {
    let [x, y] = queue.shift();
    for (let dir of dirs) {
      let i = x + dir[0],
        j = y + dir[1];
      if (
        i >= 0 &&
        i < m &&
        j >= 0 &&
        j < n &&
        matrix[i][j] > matrix[x][y] + 1
      ) {
        matrix[i][j] = matrix[x][y] + 1;
        queue.push([i, j]);
      }
    }
  }
  return matrix;
};
